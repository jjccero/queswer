package com.gzu.queswer.model;

public class Attitude {
    Long aid;
    Long uid;
    Boolean attitude;

    public Long getAid() {
        return aid;
    }

    public void setAid(Long aid) {
        this.aid = aid;
    }

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public Boolean getAttitude() {
        return attitude;
    }

    public void setAttitude(Boolean attitude) {
        this.attitude = attitude;
    }

}
